#==============================================================================
# ** Window_Help
#------------------------------------------------------------------------------
#  This window shows skill and item explanations along with actor status.
#==============================================================================
class Window_Help < Window_Base
  #--------------------------------------------------------------------------
  # * Overwrite: Set Text
  #--------------------------------------------------------------------------
  def set_text(text)
    if text != @text
      @text = text
      @text = modify_text if text.is_a?(Array)
      refresh
    end
  end
  #--------------------------------------------------------------------------
  def modify_text
    @text.inject("") do |str, line|
      str += line + 10.chr
    end
  end
  #--------------------------------------------------------------------------
end
