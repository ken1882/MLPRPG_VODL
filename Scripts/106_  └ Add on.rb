#==============================================================================
# ** Window_Base
#------------------------------------------------------------------------------
#  This is a super class of all windows within the game.
#==============================================================================
class Window_Base < Window
  #--------------------------------------------------------------------------
  # * Window visible? tag: modified
  #--------------------------------------------------------------------------
  def visible?
    return self.visible
  end
  #--------------------------------------------------------------------------
  # * Window Active?
  #--------------------------------------------------------------------------
  def active?
    return self.active
  end
end
