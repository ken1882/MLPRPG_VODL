#==============================================================================
# ** PONY::SpriteDepth
#------------------------------------------------------------------------------
#  Handles sprites depth (z) values
#==============================================================================
module PONY::SpriteDepth
  #--------------------------------------------------------------------------
  # * Return depth by layer id
  #--------------------------------------------------------------------------
  def self.layers(id)
    return 50 * (id + 1)
  end
  #--------------------------------------------------------------------------
  # * Pre-definied depth table
  #--------------------------------------------------------------------------
  Table = {
    :viewport       =>            self.layers(2),
    :viewport2      =>            self.layers(0),
    :viewport3      =>            self.layers(1),
    :character      =>                       200,
    :super          =>                      5000,
    :mouse          =>                     10000,
    :foreground_ov  =>                     99999,
    :overlays       =>                      1000,
    :huds           =>            self.layers(5),
    :input          =>                       500,
    :ballon         =>                       200,
    :animation      =>                       300,
    :parallax       =>                      -100,
    :shadow         =>                       180,
    :tile           =>                         1,
    :override       =>                       400,
    :win_info       =>                       800,
    :foreground     =>                      2000,
    :win_debug      =>                       999,
  }
  #--------------------------------------------------------------------------
end
