class Array
  #-----------------------------------------------------------------------------
  def swap_at(loc_a, loc_b)
    self[loc_a], self[loc_b] = self[loc_b], self[loc_a]
  end
  #-----------------------------------------------------------------------------
  def x; self[0]; end
  def y; self[1]; end
  def z; self[2]; end
  #-----------------------------------------------------------------------------
end