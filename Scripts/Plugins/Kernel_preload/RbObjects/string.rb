#===============================================================================
# * Basic String class
#===============================================================================
class String  
  #----------------------------------------------------------------------------
  # *) Delete a char at certain position
  #----------------------------------------------------------------------------
  def delete_at(*args)
    self.slice!(*args)
  end
  #----------------------------------------------------------------------------
  # * Delete extra empty chars via Win32API
  #----------------------------------------------------------------------------
  def purify
    #self.gsub!("\u0000", '').delete!('\\0').squeeze!('\\').tr!('\\','/').delete_at(length-1)
    self.gsub!(/(?:\u0000)(.+)/,'')
    self.gsub!('\\n', 10.chr)
    self
  end
end