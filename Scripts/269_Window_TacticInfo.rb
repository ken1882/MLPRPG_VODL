#==============================================================================
# ** Window_TacticInfo
#------------------------------------------------------------------------------
#  Very big help information window in Scene_Tactic
#==============================================================================
class Window_TacticInfo < Window_Selectable
	#--------------------------------------------------------------------------
	InitDrawX = 4
	InitDrawY = 0
	#--------------------------------------------------------------------------
	attr_reader :lines
	#--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
	def initialize(x, y, width = window_width, height = window_height)
		@lines = Vocab::Tactic::Hints
		super(x, y, width, height)	
		deactivate
		hide
	end
	#--------------------------------------------------------------------------
	def window_width
		return Graphics.width
	end
	#--------------------------------------------------------------------------
	def window_height
		return Graphics.height
	end
	#--------------------------------------------------------------------------
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
   	draw_all_items
  end
  #--------------------------------------------------------------------------
  def item_max
  	return @lines.size
  end
  #--------------------------------------------------------------------------
  def draw_item(index)
  	x = InitDrawX
  	y = InitDrawY + line_height * index
  	draw_text_ex(x, y, @lines[index])
  end
	#--------------------------------------------------------------------------
end
