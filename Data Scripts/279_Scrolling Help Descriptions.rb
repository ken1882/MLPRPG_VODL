║  "How do I change the speed of the scrolling?"                               ║
║  Right click anywhere in the script editor and select "Find" (or CTRL + F)   ║
║  search for "SPEED" (without quotation marks).                               ║
║                                                                              ║
║  You will see something like "SPEED = 0.8"                                   ║
║                                                                              ║
